function myFunction() {
  const table = document.getElementById("table");
  table.innerHTML = "";

  let bookmarkrow = "";
  let uncheckedRows = "";
  let checkedRows = "";
  let checkedCount = 0;
  let heading = `
  <tr>
    <th>Solved</th>
    <th>Id</th>
    <th>Question</th>
    <th>Link</th>
    <th>Bookmark</th>
  </tr>    

  `;

  for (let i = start; i < end; i++) {
    let quename = data[i]["Problem: "];

    let isChecked =
      localStorage.getItem(userkey + "_check_" + quename) == 1 ? "checked" : "";
    let isBookmark =
      localStorage.getItem(userkey + "_bookmark_" + quename) == 1
        ? "checked"
        : "";

    if (isChecked) checkedCount++;

    let row = `
            <tr>
                <td><input type="checkbox" class="check" data-name="${quename}" ${isChecked}></td>
                <td>${(i + 1) % start || i + 1}</td>
                <td>${quename}</td>
                <td><a href="${data[i]["URL"]}" target="_blank">
                ${data[i]["URL"]}</a>
                </td>
                
                <td>
                      <input type="checkbox" class="bookmark" id="bookmark${i}" data-name="${quename}" ${isBookmark}>
                      <label for="bookmark${i}" class="bookmark-label"><i class="fa-regular fa-bookmark"></i></label>
                </td>
            </tr>
        `;

    if (isBookmark) {
      bookmarkrow += row;
    } else if (isChecked) {
      checkedRows += row;
    } else {
      uncheckedRows += row;
    }
  }

  table.innerHTML = heading + bookmarkrow + uncheckedRows + checkedRows;

  localStorage.setItem(userkey + currentTopic + "_checkedCount", checkedCount);

  document.getElementById(
    "checkedCount"
  ).innerText = `You Solved ${checkedCount} Questions Out of ${end - start} 😄`;
}

